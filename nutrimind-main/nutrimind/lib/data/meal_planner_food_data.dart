  static List<LocalMealPlannerFood> getFoodsByMealType(String mealType) {
    final normalized = mealType.toLowerCase().trim();
    return localDavaoFoods
        .where((food) => food.mealType == normalized)
        .toList(growable: false);
  }

  static List<LocalMealPlannerFood> getBudgetFriendlyFoods({
    double maxPricePhp = 70,
  }) {
    return localDavaoFoods
        .where(
          (food) =>
              food.mealType != 'ingredient' &&
              food.estimatedPricePhp <= maxPricePhp,
        )
        .toList(growable: false);
  }

  static List<LocalMealPlannerFood> getHighProteinFoods({
    int minProteinGrams = 20,
  }) {
    return localDavaoFoods
        .where((food) => food.protein >= minProteinGrams)
        .toList(growable: false);
  }

  static List<LocalMealPlannerFood> getLocalDavaoFoods() =>
      List<LocalMealPlannerFood>.unmodifiable(localDavaoFoods);

  static LocalMealPlannerFood? getLocalFoodById(String id) {
    for (final food in localDavaoFoods) {
      if (food.id == id) return food;
    }
    return null;
  }

