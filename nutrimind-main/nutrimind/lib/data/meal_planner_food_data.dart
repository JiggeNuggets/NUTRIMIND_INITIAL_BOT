// Local prototype food data for NutriMind's BMR and budget meal planning.
//
// Nutrition and price values are estimates for prototype use only. They are
// aligned with common serving sizes and broad references such as DOST-FNRI
// Philippine Food Composition Tables/Food Exchange Lists and USDA FoodData
// Central. Verify against current labels, market prices, and professional
// nutrition guidance before using for medical decisions.
//
// Production should not treat this file as live market data. Replace or
// override these rows with Firestore `local_foods` and `market_prices` records
// that include source, sourceType, lastVerifiedDate, serving size, macros,
// price unit, and market/vendor/date metadata.

class LocalMealPlannerFood {
  const LocalMealPlannerFood({
    required this.id,
    required this.name,
    required this.mealType,
    required this.calories,
    required this.protein,
    required this.carbs,
    required this.fat,
    required this.estimatedPricePhp,
    required this.ingredients,
    required this.servingSize,
    required this.healthNote,
    required this.category,
    this.source = MealPlannerFoodData.prototypeSource,
    this.sourceType = MealPlannerFoodData.prototypeSourceType,
    this.lastVerifiedDate,
    this.isPrototypeEstimate = true,
    this.sourceNote = 'Estimated prototype values based on common serving '
        'sizes and public nutrition references including DOST-FNRI Philippine '
        'Food Composition Tables/Food Exchange Lists and USDA FoodData '
        'Central; verify before medical use.',
  });

  final String id;
  final String name;

  /// One of: breakfast, lunch, dinner, snack, ingredient.
  final String mealType;

  final int calories;
  final int protein;
  final int carbs;
  final int fat;
  final double estimatedPricePhp;
  final List<String> ingredients;
  final String servingSize;
  final String healthNote;
  final String category;

  /// Human-readable source label for audit and UI disclosure.
  final String source;

  /// Structured source category, e.g. prototype_estimate or firestore_verified.
  final String sourceType;

  /// ISO date when price/macros were verified. Null means not yet verified.
  final String? lastVerifiedDate;

  /// True until a row is backed by verified food/market data.
  final bool isPrototypeEstimate;

  final String sourceNote;
}

